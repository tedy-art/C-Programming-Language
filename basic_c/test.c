#include<stdio.h>

int main()
{
    int n=9;

    // if statement with true condition
    if(n<10) // if `n` is less than 10
    {
        printf("%d < 10", n);
    }

    // if statement with false condition
    if(n>20) // if `n` is greater than 20
    {
        printf("%d > 20", n);
    }
    return 0;
}